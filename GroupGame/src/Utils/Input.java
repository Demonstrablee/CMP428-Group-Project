package Utils;

import java.awt.event.KeyEvent;

/**
 * All keyboard and mouse inputs for the game
 * that need to be accessed throughout.
 */
public class Input {

	// WASD Movement
	public static final int W = KeyEvent.VK_W;
	public static final int A = KeyEvent.VK_A;
	public static final int S = KeyEvent.VK_S;
	public static final int D = KeyEvent.VK_D;

	// Arrow Movement
	public static final int UP = KeyEvent.VK_UP;
	public static final int DN = KeyEvent.VK_DOWN;
	public static final int LT = KeyEvent.VK_LEFT;
	public static final int RT = KeyEvent.VK_RIGHT;

	// Use Item
	public static final int U = KeyEvent.VK_U;

}
